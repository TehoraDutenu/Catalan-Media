    <h5>Els Segadors</h5>

    <p class="poem">
        Catalunya triomfant,
        Tornarà a ser rica i plena.
        Endarrera aquesta gent
        Tan ufana i tan superba.

        Bon cop de falç!
        Bon cop de falç,
        Defensors de la terra!
        Bon cop de falç!

        Ara és hora, segadors.
        Ara és hora d'estar alerta.
        Per quan vingui un altre juny,
        Esmolem ben bé les eines.
        
        Bon cop de falç!
        Bon cop de falç, 
        Defensors de la terra!
        Bon cop de falç!
    </p>

    <hr>

    <h5>Translation</h5>

    <p class="poem translation">
        Catalonia triumphant,
        Shall again be rich and bountiful.
        Drive away these people
        Who are so conceited and so contempt-full.
    
        Strike with your sickle!
        Strike with your sickle,
        Defenders of the land!
        Strike with your sickle!
    
        Now is the time, reapers.
        Now is the time to stand alert.
        For when another June comes,
        Let us sharpen well our tools.
    
        Strike with your sickle!
        Strike with your sickle,
        Defenders of the land,
        Strike with your sickle
    </p>
